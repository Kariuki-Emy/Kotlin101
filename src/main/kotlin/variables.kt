fun main(args: Array<String>) {
    //mutable variables
    var name="eMobilis Mobile Technology"
    var jina="Erick"
    var nambari=56
    var num1=5
    var num2=9
    name="University of Nairobi"

    println(name)
    println(nambari)
    println("My name is $jina")
    println("My name is" +jina)
    println("The product is: " +num1*num2)
    println("The sum is: " +num1+num2)
    println(("The difference is: ")+num2.minus(num1))
    println("The quotient is: "+num2/num1)
    //Immutable variable
    val mimi="Mr. Were"
    println("My name is $mimi")
}